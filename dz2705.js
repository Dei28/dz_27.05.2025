// Вот решение задания 5, в котором необходимо объединить два массива en и ru в объект, 
// где элементы из массива en будут ключами, а соответствующие элементы из массива ru будут значениями.

// Объявляем массивы с названиями дней на английском и русском языках
const en = ["mon", "tue", "wed", "thu", "fri", "sat", "sun"];
const ru = ["понедельник", "вторник", "среда", "четверг", "пятница", "суббота", "воскресенье"];

// Используем метод reduce для создания объекта из двух массивов
const days = en.reduce((acc, day, index) => {
    acc[day] = ru[index]; // Устанавливаем ключ и значение в объекте
    return acc; // Возвращаем аккумулятор для следующей итерации
}, {});

// Выводим результирующий объект в консоль, который содержит пары "ключ-значение"
console.log(days); // Выводит объект с днями недели

// Описание работы кода:
// Метод reduce применяется к массиву en, чтобы создать новый объект.
// Каждый элемент из массива en становится ключом в объекте, 
// а соответствующий элемент из массива ru — значением.
// Таким образом, в конце мы получаем объект, 
// который содержит пары "ключ-значение" для дней недели.




